package com.ehb.samproject;

public class Student {
	private int ID;
	private String firstName;
	private String name;
	private String password;
	private String email;
	private int number;
	private boolean isOnLine;
	
	private int classesStudentsID;
	private int schoolYearsID;
	private int startDate;
	private int endDate;
	
	private int classesID;
	private String classText;
	
	private int teachersID;

	public int getID() {
		return ID;
	}

	public void setID(int iD) {
		ID = iD;
	}

	public String getFirstName() {
		return firstName;
	}

	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public int getNumber() {
		return number;
	}

	public void setNumber(int number) {
		this.number = number;
	}

	public boolean isOnLine() {
		return isOnLine;
	}

	public void setOnLine(boolean isOnLine) {
		this.isOnLine = isOnLine;
	}

	public int getClassesStudentsID() {
		return classesStudentsID;
	}

	public void setClassesStudentsID(int classesStudentsID) {
		this.classesStudentsID = classesStudentsID;
	}

	public int getSchoolYearsID() {
		return schoolYearsID;
	}

	public void setSchoolYearsID(int schoolYearsID) {
		this.schoolYearsID = schoolYearsID;
	}

	public int getStartDate() {
		return startDate;
	}

	public void setStartDate(int startDate) {
		this.startDate = startDate;
	}

	public int getEndDate() {
		return endDate;
	}

	public void setEndDate(int endDate) {
		this.endDate = endDate;
	}

	public int getClassesID() {
		return classesID;
	}

	public void setClassesID(int classesID) {
		this.classesID = classesID;
	}

	public String getClassText() {
		return classText;
	}

	public void setClassText(String classText) {
		this.classText = classText;
	}

	public int getTeachersID() {
		return teachersID;
	}

	public void setTeachersID(int teachersID) {
		this.teachersID = teachersID;
	}
	
	
	

}
